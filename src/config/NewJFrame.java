    public void loadDirectory(String nameSearch, String deptFilter) {
        DefaultTableModel model = (DefaultTableModel) tblDirectory.getModel();
        model.setRowCount(0);
        for (Object[] row : EmployeeQuery.searchDirectory(nameSearch, deptFilter)) {
            model.addRow(row);
        }
        lblRecordCount.setText("Total Results: " + model.getRowCount());
    }

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String empIdStr = txtEmpID.getText().trim();
        String leaveType = cmbLeaveType.getSelectedItem().toString();
        java.util.Date startDate = jdStart.getDate();
        java.util.Date endDate = jdEnd.getDate();

        String error = LeaveLogic.validate(empIdStr, leaveType, startDate, endDate, selectedPath);
        if (error != null) {
            JOptionPane.showMessageDialog(this, error);
            return;
        }

        int empId = Integer.parseInt(empIdStr);
        String empName = LeaveQuery.getEmployeeNameById(empId);
        if (empName == null) {
            JOptionPane.showMessageDialog(this, "Employee ID not found!");
            return;
        }

        long daysOfLeave = (endDate.getTime() - startDate.getTime()) / (24 * 60 * 60 * 1000);
        String medCert = (leaveType.equals("SL") && daysOfLeave >= 1) ? selectedPath : "None";

        if (LeaveQuery.submitRequest(empId, leaveType, startDate, endDate, medCert)) {
            JOptionPane.showMessageDialog(this, "Leave filed successfully for " + empName + "!");
            selectedPath = "None";
            lblFilePath.setText("No file selected");
            displayPendingRequests();
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveActionPerformed
        int row = tblPendingRequests.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a request first!");
            return;
        }

        String requestId = tblPendingRequests.getValueAt(row, 0).toString();
        String empName = tblPendingRequests.getValueAt(row, 2).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Approve leave for " + empName + "?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION && LeaveQuery.updateStatus(requestId, "Approved")) {
            JOptionPane.showMessageDialog(this, "Leave for " + empName + " approved!");
            displayPendingRequests();
        }
    }//GEN-LAST:event_btnApproveActionPerformed

    private void btnDisapproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisapproveActionPerformed
        int row = tblPendingRequests.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a request first!");
            return;
        }

        String requestId = tblPendingRequests.getValueAt(row, 0).toString();
        if (LeaveQuery.updateStatus(requestId, "Disapproved")) {
            JOptionPane.showMessageDialog(this, "Request #" + requestId + " disapproved.");
            displayPendingRequests();
        }
    }//GEN-LAST:event_btnDisapproveActionPerformed

