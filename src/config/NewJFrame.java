    public void handleUserSession(String name, String role, int id) {
        this.currentUserName = name;
        this.currentUserRole = role;
        lblWelcomeStatus.setText("User: " + name + " (" + role + ")");
        lblWelcomeStatus1.setText("User: " + name + " (" + role + ")");
        txtEmpID.setText(String.valueOf(id));
        txtEmpID.setEditable(SessionLogic.isEmpIdEditable(role));

        Object[] emp = EmployeeQuery.getById(id);
        if (emp != null) {
            txtProfileID.setText(String.valueOf(emp[0]));
            txtProfileName.setText((String) emp[1]);
            txtProfileDept.setText((String) emp[2]);
            txtProfilePos.setText((String) emp[3]);
            txtProfileContact.setText((String) emp[4]);
            txtProfileID1.setText(String.valueOf(emp[0]));
            txtProfileName1.setText((String) emp[1]);
            txtProfileDept1.setText((String) emp[2]);
            txtProfilePos1.setText((String) emp[3]);
            txtProfileContact1.setText((String) emp[4]);
            txtProfileRole1.setText((String) emp[5]);
        }

        if (SessionLogic.isEmployee(role)) {
            jTabbedPane2.remove(4);
            jTabbedPane2.remove(3);
            jTabbedPane2.remove(2);
            btnNavReports.setVisible(false);
            btnNavManagement.setVisible(false);
            btnNavDirectory.setVisible(false);
        }
        jTabbedPane2.setSelectedIndex(SessionLogic.getStartingTabIndex(role));
    }


